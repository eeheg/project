const bycHand=document.getElementsByClassName("byc_hand")[0]
bycHand.addEventListener("click", () => {
    document.querySelector("body").style.backgroundPosition = "-1186px";
    
    document.querySelector(".content").style.animation = "fadeout 1s";
    setTimeout(() => {
        document.querySelector(".content").style.opacity = "0";
    }, 900);
    document.querySelector(".buyone").style.transform = "translate(0px, 200px)";
})

